    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblFAQs.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to update!");
            return;
        }
        
          
            DefaultTableModel model = (DefaultTableModel) tblFAQs.getModel();
            FAQ p = (FAQ) model.getValueAt(selectedRowIndex, 0);
            ArrayList<FAQ> latestDirectory = faqDirectory.getFaqDirectory();
            for (FAQ obj : latestDirectory) {
                obj.setCategory(comboCategory.getSelectedItem().toString());
                obj.setFAQlink(txtFAQLink.getText());
                
                JOptionPane.showMessageDialog(this, "Record Updated!");
                
                
            }
            
            btnUpdate.setEnabled(false);
        clearFields();
        populateTable();
        enableFields(false);
            
        
    }//GEN-LAST:event_btnUpdateActionPerformed

