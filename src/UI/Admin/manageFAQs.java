    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblFAQs.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to edit!");
            return;
        } else {
            enableFields(true);
            btnUpdate.setEnabled(true);
            btnPost.setEnabled(false);
            DefaultTableModel model = (DefaultTableModel) tblFAQs.getModel();
            FAQ p = (FAQ) model.getValueAt(selectedRowIndex, 0);
            comboCategory.setSelectedItem(p.getCategory().toString());
            txtFAQLink.setText(p.getFAQlink());
            
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblFAQs.getSelectedRow();
        
        if(selectedRowIndex<0){
        JOptionPane.showMessageDialog(this, "Please select a row to delete");
        return;
        }
        DefaultTableModel model  = (DefaultTableModel)tblFAQs.getModel();
        FAQ selctedFAQ = (FAQ) model.getValueAt(selectedRowIndex, 0);
        
        faqDirectory.deleteCar(selctedFAQ);
        JOptionPane.showMessageDialog(this, "FAQ deleted");
        
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


